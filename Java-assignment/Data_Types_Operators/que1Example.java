public class que1Example {
        public static void main(String[] args) {
            // Primitive Data Types (store actual values)
            int age = 20;
            double height = 5.8;
            char grade = 'A';
            boolean isPassed = true;
    
            // Reference Data Type (stores address of an object)
            String name = "Alice"; // String is a reference type
    
            // Printing Primitive Data
            System.out.println("Primitive Data:");
            System.out.println("Age: " + age);
            System.out.println("Height: " + height);
            System.out.println("Grade: " + grade);
            System.out.println("Passed: " + isPassed);
    
            // Printing Reference Data
            System.out.println("\nReference Data:");
            System.out.println("Name: " + name);
        }
    }
    
    

